-- 3 types of relationship
-- 1. one to one relationship
-- 2. one to many relationship
-- 3. many to many relationship

-- 1. one to one relationship: when a single row in one table is related to a single row in another table
-- 2. one to many relationship: when a single row in one table is related to multiple rows in another table
-- 3. many to many relationship: when multiple rows in one table are related to multiple rows in another table

------------------------ Foreign Key ------------------------

-- When we use a primary key of a table in another table, it is known as a foreign key.